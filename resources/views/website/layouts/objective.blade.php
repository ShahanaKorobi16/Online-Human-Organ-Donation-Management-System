<h1><center><span style="color:#2874A6">Vission of Organ Donation</span></center></h1>



<h5>

The primary objective is to promote awareness of life-saving solid organ transplants. 
The secondary objective is to promote awareness of tissue and life-enhancing transplants.
Main objectives of organ donations are:<br>

1. to educate the public about organ and tissue donation;<br>
2. to engender a greater willingness amongst members of the public to donate their organs and tissue;<br>
3. to significantly increase the number of members of the public registered as organ and tissue donors with the ODF, and to record these registered organ / tissue donors on an easily accessible registry/database which will be used as a means of measuring the success of organ and tissue donor awareness drives;<br>
4. to assist medical professionals and stakeholders responsible for donor identification by helping with financial assistance towards organ donor and tissue referral programmes and workshops, such assistance shall be provided at the Board's sole discretion and only if it is financially viable for the ODF to do so;<br>
5. where possible, the Board, acting in its sole discretion, shall assist in advocating the challenges of organ transport and delivery and to increase the number of possible transport and delivery incidences that are required, and to do all such things as are consistent with the aforegoing objectives and aims.<br>


